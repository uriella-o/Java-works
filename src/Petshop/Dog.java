package Petshop;

public class Dog {
        private String name;
        private String breed;
        private String sex;
        private int age;


        public void setname(String name) {
            this.name = name;
        }

        public void setbreed(String breed) {
            this.breed = breed;

        }

        public void setSex(String sex) {
            this.sex = sex;

        }

        public void setAge(int age) {
            this.age = age;

        }

        public String getname() {
            return this.name;
        }

        public String getbreed() {
            return this.breed;
        }

        public String getSex() {
            return this.sex;
        }

        public int getAge() {
            return this.age;
        }
    }

